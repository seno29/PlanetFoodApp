    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        int v= validateInput();
        if(v==1){
            Employee e = new Employee();
            e.setEmpId(empId);
            e.setEname(ename);
            e.setSal(sal);
            e.setEmpType(etype);
            
            try{
                boolean status = EmployeeDao.addEmployee(e);
                if(status){
                    JOptionPane.showMessageDialog(null,"EMPLOYEE ADDED","Success!",JOptionPane.INFORMATION_MESSAGE);
                    clearAll();
                }else{
                    JOptionPane.showMessageDialog(null,"employee not added","Exception!",JOptionPane.ERROR_MESSAGE);
                }
            }catch(SQLException ex){
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null,"Error in db","Exception!",JOptionPane.ERROR_MESSAGE);
            }
        }else if(v==0){
            JOptionPane.showMessageDialog(null,"Enter a Emp Name","Exception!",JOptionPane.ERROR_MESSAGE);
        }else if(v==-1){
            JOptionPane.showMessageDialog(null,"Enter a valid salary","Exception!",JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_btnRegisterActionPerformed

