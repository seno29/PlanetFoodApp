    public int validateInput(){
        empId = txtEmpid.getText();
        ename = txtEname.getText();
        etype = jcJob.getSelectedItem().toString();
        try{
            sal = Double.parseDouble(txtSal.getText());
        }catch(Exception e){
//            JOptionPane.showMessageDialog(null,"Enter a valid salary","Exception!",JOptionPane.ERROR_MESSAGE);
            return -1;
        }
        if(ename.isEmpty()){
//            JOptionPane.showMessageDialog(null,"Enter a Emp Name","Exception!",JOptionPane.ERROR_MESSAGE);
            return 0;
        }
        return 1;
    }
