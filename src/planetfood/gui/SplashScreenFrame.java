    class SplashThread extends Thread{
        public void run(){
            int count = 1;
            Random r = new Random();
            while(jP1.getValue() < jP1.getMaximum()){
                try{
                jP1.setValue((int)count);
                Thread.sleep(1200);
                count = count + r.nextInt(100);
                System.out.println(count);
                }catch(InterruptedException e){
                    JOptionPane.showMessageDialog(null,"Exception in Thread:"+e,"Error!",JOptionPane.ERROR_MESSAGE);
                    e.printStackTrace();
                }
            }
            SplashScreenFrame.this.dispose();
            LoginFrame ob = new LoginFrame();
            ob.setVisible(true);
        }
    }
    
    
